#python support various data types
# integer (int  - whole numbers) e.g., 1, 2, 3
# float (floating point numbers - decimal numbers) e.g., 1.5, 2.75
# string (str - sequence of characters) e.g., "Hello", 'Python'
# boolean (bool - True or False) e.g., True, False      
#list (list - ordered collection of items) e.g., [1, 2, 3], ['a', 'b', 'c']
#tuple (tuple - ordered, immutable collection of items) e.g., (1, 2, 3), ('x', 'y', 'z')
#dictionary (dict - key-value pairs) e.g., {'name': 'Alice', 'age': 25}
#set (set - unordered collection of unique items) e.g., {1, 2,
age = 20 # interger
print(age)
print(type(age))

cgpa = 8.5 # float
print(cgpa)
print(type(cgpa))

string = ("Hello, Python!") # string
print(string)
print(type(string))

